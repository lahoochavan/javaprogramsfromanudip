	package SwitchX;
	
	public class Arithmetic {
	
		public static void main(String[] args) {
			
		// Addition
		int a = 10;
		int b = 5;
		int c = a + b;
		System.out.println("a+b : "+c);
		
		//Multiplication
		int x = 10;
		int y = 5;
		int z = x*y;
		System.out.println("x*y : "+z);
		
		//Division
		int q = 10;
		int r = 5;
		int s = q/r;
		System.out.println("q/r : "+s);
		
		//Subtraction
		int h = 10;
		int i = 5;
		int j = h-i;
		System.out.println("h-i : "+j);
		
		//Modulo
		int d = 10;
		int e = 5;
		int f = d%e;
		System.out.println("d%e : "+f);
		
		}
	
	}
