package com.example;

public class SwitchVowel {

	public static void main(String[] args) {

		char a = 'z';
		switch (a) {
		case 'a':
			System.out.println("vowel");
			break;

		case 'b':
			System.out.println("vowel");
			break;

		case 'c':
			System.out.println("vowel");
			break;

		case 'd':
			System.out.println("vowel");
			break;

		case 'e':
			System.out.println("vowel");
			break;

		default:
			System.out.println("None of the above");
		}
	}

}
